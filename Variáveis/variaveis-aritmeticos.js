let x = 10, y = 2;

let resultado = x + y;
console.log("A soma de 10 + 2 é", resultado); //12

resultado = x - y;
console.log("A subtração de 10 - 2 é", resultado); //8


resultado = x * y;
console.log("A multiplicação de 10 x 2 é", resultado); //20

resultado = x / y;
console.log("A divisão de 10 / 2 é", resultado); //5